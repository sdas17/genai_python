# add(), subtract(), multiply(), divide()
def add(a:int,b:int):
    return a+b
def subtract(a:int,b:int):
    return a-b
def multiply(a:int,b:int):
    return a*b
def divide(a:int,b:int):
    return a/b

if __name__ == "__main__":
    print(add(1,2))
    print(subtract(1,2))
    print( multiply(1,2))
    print( divide(1,2))
   
